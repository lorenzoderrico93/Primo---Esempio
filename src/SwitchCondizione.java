
public class SwitchCondizione {

	public static void main(String[] args) {
	
		
		//Esercizio 1
		int num = 2;
		
		switch ( num ) {
		case 0:
			//blocco di istruzioni del caso 0
			System.out.println("Num è 0");
			break;
			
		case 1:
			//blocco di istruzioni del caso 1
			System.out.println("Num è 1");
			break;
			
		case 2:
			//blocco di istruzioni del caso 2
			System.out.println("Num è 2");
			break;
			
		default:
			System.out.println("Non sono riuscito a determinare il  valore di Num");
			
			
		}
		
		
		

	}

}
